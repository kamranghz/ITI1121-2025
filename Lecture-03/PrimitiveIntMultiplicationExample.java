public class PrimitiveIntMultiplicationExample {
    public static void main(String[] args) {
        // Declare two integer primitives
        int number1 = 33; // First integer value
        int number2 = 5.0;  // Second integer value

        // Perform multiplication between two integers
        int result = number1 * number2;

        // Print the integer values
        System.out.println("The first integer value: " + number1);
        System.out.println("The second integer value: " + number2);

        // Print the result of multiplication
        System.out.println("The result of multiplication: " + result);
    }
}
