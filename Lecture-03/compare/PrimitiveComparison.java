// This class demonstrates comparisons between primitive types in Java.
public class PrimitiveComparison {
    public static void main(String[] args) {
        int a = 5; // Primitive variable 'a' initialized with the value 5.
        int b = 10; // Primitive variable 'b' initialized with the value 10.

        // Comparing primitive values using relational operators.
        if (a < b) {
            System.out.println("a < b"); 
        } else if (a == b) {
            System.out.println("a == b");
        } else {
            System.out.println("a > b");
        }
    }
}
