class ScopeExample5 {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.println("i inside first loop: " + i);
        }

        
        for (int i = 10; i <= 15; i++) {
            System.out.println("i inside second loop: " + i);
        }
    }
}
